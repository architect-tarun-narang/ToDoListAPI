using System;
using System.Collections.Generic;
using System.Linq;
using System.Net.Http;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Http;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Extensions.Logging;

namespace TodoListAPI.Controllers
{
    [ApiController]
    [Route("[controller]")]
    public class WeatherForecastController : ControllerBase
    {
        private static readonly string[] Summaries = new[]
        {
            "Freezing", "Bracing", "Chilly", "Cool", "Mild", "Warm", "Balmy", "Hot", "Sweltering", "Scorching"
        };

        private readonly ILogger<WeatherForecastController> _logger;

        public WeatherForecastController(ILogger<WeatherForecastController> logger)
        {
            _logger = logger;
        }

        [HttpGet]
        public IEnumerable<WeatherForecast> Get()
        {
            var rng = new Random();
            return Enumerable.Range(1, 5).Select(index => new WeatherForecast
            {
                Date = DateTime.Now.AddDays(index),
                TemperatureC = rng.Next(-20, 55),
                Summary = Summaries[rng.Next(Summaries.Length)]
            })
            .ToArray();
        }

        [HttpGet]
        [Route("GetAPI2")]
        public async Task<string> GetAPI2()
        {
            HttpClient client = new HttpClient();
            var defaultRequestHeaders = client.DefaultRequestHeaders;
            //string webApiUrl = "https://localhost:44352/api/todolist2/getAllTasks2";
            string webApiUrl = "https://localhost:44352/WeatherForecast";
            
            HttpResponseMessage response = await client.GetAsync(webApiUrl);
            string json = await response.Content.ReadAsStringAsync();
            Console.WriteLine("JSON = " + json);

            return json;
        }

    }
}